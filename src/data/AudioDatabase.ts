// Helper functions for filtering and searching
export const getTracksByCategory = (category: AudioTrack['category']) => {
  return audioDatabase.filter((track) => track.category === category);
};

export const getTracksByTier = (tier: AudioTrack['tier']) => {
  return audioDatabase.filter((track) => track.tier === tier);
};

export const getTracksByRecommendation = (
  recommendation: 'meditation' | 'sleep' | 'focus' | 'relaxation' | 'energy'
) => {
  return audioDatabase.filter((track) =>
    track.recommendedFor.includes(recommendation)
  );
};

export const getTracksBySubcategory = (subcategory: string) => {
  return audioDatabase.filter((track) => track.subcategory === subcategory);
};

export const searchTracks = (query: string) => {
  const lowerQuery = query.toLowerCase();
  return audioDatabase.filter(
    (track) =>
      track.title.toLowerCase().includes(lowerQuery) ||
      track.description.toLowerCase().includes(lowerQuery) ||
      track.tags.some((tag) => tag.toLowerCase().includes(lowerQuery)) ||
      track.benefits.some((benefit) => benefit.toLowerCase().includes(lowerQuery))
  );
};

